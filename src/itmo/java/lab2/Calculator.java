package itmo.java.lab2;

public class Calculator {

    public double amount(double x, double y) {
        return x + y;
    }

    public long amount(long x, long y) {
        return x + y;
    }

    public int amount(int x, int y) {
        return x + y;
    }

    public double divided(double x, double y) {
        return x / y;
    }

    public double divided(long x, long y) {
        return (double) x / (double) y;
    }

    public double divided(int x, int y) {
        return (double) x / (double) y;
    }

    public double multiplication(double x, double y) {
        return x * y;
    }

    public long multiplication(long x, long y) {
        return x * y;
    }

    public int multiplication(int x, int y) {
        return x * y;
    }

    public double subtraction(double x, double y) {
        return x - y;
    }

    public long subtraction(long x, long y) {
        return x - y;
    }

    public int subtraction(int x, int y) {
        return x - y;
    }
}
